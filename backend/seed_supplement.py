import sqlite3, json
from datetime import datetime, timezone

DB = 'trainer.db'
now = datetime.now(timezone.utc).isoformat()
conn = sqlite3.connect(DB)
cur = conn.cursor()

added = 0

def add_exercises(module, etype, entries):
    global added
    for content, question, options, answer, explanation in entries:
        cur.execute("""INSERT INTO exercises
            (module, type, title, content, question, options_json, answer, explanation, extra_json, created_at, source, status)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)""",
            (module, etype, '', content, question,
             json.dumps(options, ensure_ascii=False),
             answer, explanation,
             json.dumps({}, ensure_ascii=False),
             now, 'seed_supplement', 'active'))
        added += 1
    conn.commit()
    print(f"  +{len(entries)} {module}/{etype}")

