def seed_translation(conn):
    """仅在 translation 数据为空时导入。"""
    existing = conn.execute(
        "SELECT COUNT(*) FROM exercises WHERE module='classical_reading' AND type='translation'"
    ).fetchone()
    if existing and existing[0] > 0:
        return

    now_ts = datetime.now(timezone.utc).isoformat()
    cur = conn.cursor()
    count = 0
    for (sentence, word, meaning, method, note) in translations:
        question_text = f"翻译此句，重点解释「{word}」的含义。"
        answer_text = f"「{word}」意为「{meaning}」。"
        explanation_text = (
            f"【第一步·字字落实】重点词「{word}」={meaning}，逐字对应现代汉语。\n"
            f"【第二步·句法还原】识别原句句式，还原为现代汉语语序。\n"
            f"【第三步·规范译文】{note}"
        )
        cur.execute("""INSERT INTO exercises 
            (module, type, title, content, question, options_json, answer, explanation, extra_json, created_at, source, status)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)""",
            ('classical_reading', 'translation', '', sentence,
             question_text,
             json.dumps([], ensure_ascii=False),
             answer_text, explanation_text,
             json.dumps({"word": word, "meaning": meaning, "method": method}, ensure_ascii=False),
             now_ts, 'wangli_seed', 'active'))
        count += 1
    conn.commit()
    print(f"  ✅ seed_translation: 导入 {count} 道翻译题")


