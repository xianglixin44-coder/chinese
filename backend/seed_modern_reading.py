def classify_modern(text):
    lit_kw = ['他站在','她坐在','暮色','巷子','眼泪','妈妈','父亲','爷爷','奶奶','那只',
              '他走','她走','小狗','猫','风从','雪花','月亮','夕阳','雨','花开了',
              '握着','攥着','望着','窗外的','故乡','童年']
    prac_kw = ['据报道','数据显示','调查显示','统计','通知','公告','本报讯',
               '记者从','根据最新','年度报告']
    for kw in lit_kw:
        if kw in text: return 'literary'
    for kw in prac_kw:
        if kw in text: return 'practical'
    return 'discourse'

