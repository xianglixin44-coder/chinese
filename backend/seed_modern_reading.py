def seed_modern_reading(conn):
    """分类现有 choice 题型 + 导入新题（仅在新题为空时执行插入）。"""
    now_ts = datetime.now(timezone.utc).isoformat()
    cur = conn.cursor()

    # 1. 分类现有 choice 题为 discourse/literary/practical
    existing_rows = cur.execute(
        "SELECT id, content FROM exercises WHERE module='modern_reading' AND type='choice'"
    ).fetchall()
    moved = {'discourse': 0, 'literary': 0, 'practical': 0}
    for row in existing_rows:
        new_type = classify_modern(row['content'] or '')
        cur.execute("UPDATE exercises SET type=? WHERE id=?", (new_type, row['id']))
        moved[new_type] += 1
    if sum(moved.values()) > 0:
        print(f"  📦 seed_modern_reading: 重分类 {moved}")

    # 2. 仅在无对应题型数据时插入新题
    existing_count = cur.execute(
        "SELECT COUNT(*) FROM exercises WHERE module='modern_reading' AND type IN ('discourse','literary','practical')"
    ).fetchone()[0]
    if existing_count > 0 and sum(moved.values()) == 0:
        return  # 已有数据且非初次迁移

    for passages, ptype in [(literary_passages, 'literary'), (practical_passages, 'practical'), (discourse_passages, 'discourse')]:
        type_count = cur.execute(
            "SELECT COUNT(*) FROM exercises WHERE module='modern_reading' AND type=?", (ptype,)
        ).fetchone()[0]
        if type_count >= len(passages):
            continue  # 此类型已有足够数据
        for content, question, options, answer, explanation in passages:
            cur.execute("""INSERT INTO exercises 
                (module, type, title, content, question, options_json, answer, explanation, extra_json, created_at, source, status)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)""",
                ('modern_reading', ptype, '', content,
                 question,
                 json.dumps(options, ensure_ascii=False),
                 answer, explanation,
                 json.dumps({}, ensure_ascii=False),
                 now_ts, 'seed', 'active'))
        print(f"  ✅ seed_modern_reading: {ptype} 导入 {len(passages)} 题")

    conn.commit()


