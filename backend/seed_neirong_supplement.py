    existing = conn.execute(
        "SELECT COUNT(*) FROM exercises WHERE module='classical_reading' AND type='neirong'"
    ).fetchone()
    if existing and existing[0] > 0:
        return

    now_ts = datetime.now(timezone.utc).isoformat()
    cur = conn.cursor()

    # Fix 2 existing questions (if they exist)
    fixes = [
        (6855, fix_6855_content, "宋史·苏轼传"),
        (6856, fix_6856_content, "史记·屈原贾生列传"),
    ]
    for id_, content, src in fixes:
        row = cur.execute("SELECT id FROM exercises WHERE id=?", (id_,)).fetchone()
